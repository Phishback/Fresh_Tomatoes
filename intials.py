import turtle

def draw_name():
    window = turtle.Screen()
    window.bgcolor("black")

    e = turtle.Turtle()
    e.shape("arrow")
    e.color("white")
    e.speed(5)
    
    e.right(90)
    e.forward(100)
    e.left(90)
    e.forward(100)
    e.right(180)
    e.forward(100)
    e.right(90)
    e.forward(100)
    e.right(90)
    e.forward(50)
    e.right(180)
    e.forward(50)
    e.right(90)
    e.forward(100)
    e.right(90)
    e.forward(100)
    

draw_name()
